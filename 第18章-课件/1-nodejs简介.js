/*
 * nodejs是在ecmascript的基础上扩展出了一些操作非浏览器的方法
 * nodejs与js的语法都是一样的，只不过做的事情不一样，js是操作浏览器的，nodejs是操作网络系统、文件系统、数据库系统等
 * 
 * 下载
 * 		https://nodejs.org/en/download/
 * 		windows选择 (.msi)
 * 		mac选择 (.pkg)
 * 运行
 * 		1、在安装文件下有一个绿色的图标叫node.exe
 * 			可以点击这个图标运行
 * 			输入node -v	
 * 		2、按下win+R键，输入cmd打开命令提示符
 * 			如果用命令行的话推荐使用这个
 * 			运行nodejs的文件：node 1.js
 * 		3、通过编辑器的插件
 * 			安装插件
 * 				工具 → 插件安装 → nodeclipse
 * 			在hbuilder里编辑
 * 				需要提示：右键js文件 → 打开方式 → Nodeclipse TextEditor-based Editor
 * 				不需要提示：与编辑原生js的文件一样打开
 * 			在hbuilder里运行
 * 				右键js文件 → 运行方式 → 1-NodeApplication
 */